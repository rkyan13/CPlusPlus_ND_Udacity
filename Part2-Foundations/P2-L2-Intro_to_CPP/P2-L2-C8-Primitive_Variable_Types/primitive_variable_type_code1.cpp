
#include <iostream>
#include <string>
using std::cout;

int main() {
    // Declaring and initializing an int variable.
    int a = 9;
    
    // Declaring a string variable without initializing immediately.
    std::string b;
    
    // Initializing the string b in the next step.
    b = "Here is a string";
    
    cout << a << "\n";
    cout << b << "\n";
}



